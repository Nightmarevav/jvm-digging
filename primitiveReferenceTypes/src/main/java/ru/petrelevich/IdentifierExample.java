package ru.petrelevich;

public class IdentifierExample {
    public static void main(String[] args) {
        int String = 1; // Внезапный пример
        System.out.println("String:" + String);

        int _x = 2;
        System.out.println("_x:" + _x);

        int _3 = 3;
        System.out.println("_3:" + _3);

        //int @d = 4;  //неверно
        //int #d = 4;  //неверно
        //int 3d = 4;  //неверно
        //int :d = 4;  //неверно

        int $rr = 4;
        System.out.println("$rr:" + $rr);

        int и_так_тоже_можо = 5;
        System.out.println("и_так_тоже_можо:" + и_так_тоже_можо);

        int counter = 5;
        System.out.println("counter:" + counter);
    }
}
